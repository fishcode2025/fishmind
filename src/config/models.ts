/**
 * 根据模型获取对应的图标
 * @param model 模型对象
 * @param isDarkMode 是否为深色模式
 * @returns 图标路径
 */
export function getModelIcon(model: Model, isDarkMode: boolean = false): string {
  if (!model) return '';
  
  const { id, provider, group_id } = model;
  const iconType = isDarkMode ? 'dark' : 'light';
  
  // 1. 首先尝试通过模型ID前缀匹配
  for (const [prefix, icons] of Object.entries(MODEL_ID_PREFIX_TO_ICON)) {
    if (id.toLowerCase().includes(prefix.toLowerCase())) {
      return icons[iconType];
    }
  }
  
  // 2. 尝试通过group_id匹配
  if (group_id && GROUP_ID_TO_ICON[group_id]) {
    return GROUP_ID_TO_ICON[group_id][iconType];
  }
  
  // 3. 默认图标
  return isDarkMode ? ChatGptModelLogoDakr : ChatGptModelLogo;
}

/**
 * 根据模型ID获取对应的图标
 * @param modelId 模型ID
 * @param isDarkMode 是否为深色模式
 * @returns 图标路径
 */
export function getModelIconById(modelId: string, isDarkMode: boolean = false): string {
  if (!modelId) return '';
  
  const iconType = isDarkMode ? 'dark' : 'light';
  
  // 尝试通过模型ID前缀匹配
  for (const [prefix, icons] of Object.entries(MODEL_ID_PREFIX_TO_ICON)) {
    if (modelId.toLowerCase().includes(prefix.toLowerCase())) {
      return icons[iconType];
    }
  }
  
  // 默认图标
  return isDarkMode ? ChatGptModelLogoDakr : ChatGptModelLogo;
}

/**
 * 根据模型组ID获取对应的图标
 * @param groupId 模型组ID
 * @param isDarkMode 是否为深色模式
 * @returns 图标路径
 */
export function getGroupIcon(groupId: string, isDarkMode: boolean = false): string {
  if (!groupId) return '';
  
  const iconType = isDarkMode ? 'dark' : 'light';
  
  if (GROUP_ID_TO_ICON[groupId]) {
    return GROUP_ID_TO_ICON[groupId][iconType];
  }
  
  // 默认图标
  return isDarkMode ? ChatGptModelLogoDakr : ChatGptModelLogo;
}